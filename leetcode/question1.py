"""
给定数0,1,2,3,4, 最大数为N
数组L: 0, 2, 2, 1,4, 1, 2, 2, 长度为n
得到不在L中的数3
"""

# O(n)
def find_num1(N, L):
    for j in L:
        if j not in N:
            return j

# 将L中的数用二进制表示后,将每位数相加后,如果N==2**K - 1时,对于K位数,每一位的1的个数,如果都出现至少一次的话,对于第k位应该都大于(N + 1) / (2 ** k) 次, 之后一步一步做按位运算就能得到

N1 = [0, 1,2,3,4]
L1 = [0,2,2,1,4,1,2,2]
L2 = [2, 2, 4, 4, 0, 1, 0, 1, 2]
print(find_num1(N1, L1))
print(find_num1(N1, L2))
